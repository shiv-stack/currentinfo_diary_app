import '../../domain/entities/staff.dart';

class StaffModel extends Staff {
  const StaffModel({
    super.staffImage,
    super.name,
    super.designation,
    super.dob,
    super.contactNumber,
    super.cdiaryId,
    super.schoolName,
    super.address,
    super.email,
    super.schoolCode,
    super.password,
    super.feedbackUrl,
    super.attendanceUrl,
    super.controlSms,
    super.month,
    super.behaviourUrl,
    super.checkPlanner,
    super.uploadMarks,
    super.checkAssignmentUrl,
    super.assignmentUrl,
    super.galleryNewUrlGlide,
    super.galleryUrl,
    super.vts,
    super.costExpense,
    super.contactSchool,
    super.returnStatus,
    super.tokenMainHit,
    super.getPassword,
  });

  factory StaffModel.fromJson(Map<String, dynamic> json) {
    return StaffModel(
      staffImage: json['student_image']?.toString(), // API often uses same keys
      name: json['Name']?.toString(),
      designation: json['Designation']?.toString(),
      dob: (json['dob'] ?? json['dateofbirth'])?.toString(),
      contactNumber: json['Contact number']?.toString(),
      cdiaryId: json['cdiaryid']?.toString(),
      schoolName: json['SchoolName']?.toString(),
      address: json['Address']?.toString(),
      email: json['email']?.toString(),
      schoolCode: json['school_code']?.toString(),
      password: json['pass']?.toString(),
      feedbackUrl: json['feedback']?.toString(),
      attendanceUrl: json['attendance_url']?.toString(),
      controlSms: json['control-sms']?.toString(),
      month: json['month']?.toString(),
      behaviourUrl: json['behaviour_url']?.toString(),
      checkPlanner: json['check-planner']?.toString(),
      uploadMarks: json['upload-marks']?.toString(),
      checkAssignmentUrl: json['check_assignment_url']?.toString(),
      assignmentUrl: json['assignment_url']?.toString(),
      galleryNewUrlGlide: json['gallery_new_url_glide']?.toString(),
      galleryUrl: json['gallery_url']?.toString(),
      vts: json['vts']?.toString(),
      costExpense: json['costexpense']?.toString(),
      contactSchool: json['contact-school']?.toString(),
      returnStatus: json['return']?.toString(),
      tokenMainHit: json['token_main_hit']?.toString(),
      getPassword: json['getpassword']?.toString(),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'student_image': staffImage,
      'Name': name,
      'Designation': designation,
      'dob': dob,
      'Contact number': contactNumber,
      'cdiaryid': cdiaryId,
      'SchoolName': schoolName,
      'Address': address,
      'email': email,
      'school_code': schoolCode,
      'pass': password,
      'feedback': feedbackUrl,
      'attendance_url': attendanceUrl,
      'control-sms': controlSms,
      'month': month,
      'behaviour_url': behaviourUrl,
      'check-planner': checkPlanner,
      'upload-marks': uploadMarks,
      'check_assignment_url': checkAssignmentUrl,
      'assignment_url': assignmentUrl,
      'gallery_new_url_glide': galleryNewUrlGlide,
      'gallery_url': galleryUrl,
      'vts': vts,
      'costexpense': costExpense,
      'contact-school': contactSchool,
      'return': returnStatus,
      'token_main_hit': tokenMainHit,
      'getpassword': getPassword,
    };
  }

  Staff toEntity() => this;
}
